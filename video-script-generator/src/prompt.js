function buildScriptPrompt(data) {
  return `你是一位资深游戏宣发短视频编导，擅长把游戏卖点改造成 10-15 秒微剧情广告。

请基于以下游戏信息，生成一个适合宣传游戏的短视频脚本方案。

【游戏信息】
游戏名：${data.gameName}
游戏类型：${data.gameGenre}
核心卖点：${data.coreSellingPoint}
目标玩家：${data.targetPlayers}
视频时长：${data.duration}
视频比例：${data.ratio}
视频风格：${data.styleLabel}
补充说明：${data.extraNotes || "无"}

【核心目标】
不要写成卖点罗列。必须设计一个能在 ${data.duration} 内看懂的小剧情，让观众经历：
开场反常/危机 -> 冲突升级 -> 玩家关键选择 -> 爆点反转 -> 游戏名/行动召唤。

【15 秒微剧情结构参考】
如果用户填写的时长接近 15 秒，优先按这个节奏设计：
1. 0-2 秒：反常开场或危机钩子。必须让人立刻想看下一秒。
2. 2-5 秒：抛出小冲突。普通办法失败，局面变糟。
3. 5-9 秒：玩家做出关键选择。体现核心玩法或卖点。
4. 9-13 秒：爆点反转。画面、音效、字幕同时给高潮。
5. 13-15 秒：游戏名 + CTA。短、有记忆点，不拖沓。

如果用户填写的不是 15 秒，也要保持同样的剧情弧线，只调整每段时长。

【画面与比例要求】
1. 如果是竖屏比例，强调主体居中、字幕安全区、手机端可读性、近景冲击。
2. 如果是横屏比例，强调场景宽度、镜头调度、环境压迫感和大场面。
3. 每个镜头必须能被剪辑执行，不要写抽象概念。

【文案要求】
1. 输出中文。
2. 字幕和口播要短句化，适合短视频，不要长段解释。
3. 避免“超爽、极致、震撼”等空泛词，必须用具体画面证明爽点。
4. 允许有反差、悬念、失败、误判、反杀，但不要脱离游戏卖点。

【必须输出以下栏目】

## 3 个微剧情方向
先给出 3 个不同方向，每个方向包含：
- 剧情名
- 2 秒钩子
- 冲突
- 反转爆点
- 适合原因

## 推荐采用的方向
从 3 个方向中选 1 个最适合当前游戏宣传的方向，并说明原因。

## 一句话爆点
一句 20 字以内的核心钩子。

## 逐秒分镜表
按时间段拆分镜头。每行必须包含：
- 时间
- 剧情作用：钩子/冲突/选择/反转/CTA
- 画面
- 口播
- 屏幕字幕
- 音效/剪辑点

## 完整口播/字幕稿
给出一版可以直接使用的完整短文案。必须控制在 ${data.duration} 能读完。

## 屏幕大字
列出 3-6 条适合叠在画面上的短句，每条不超过 8 个字。

## 音效与剪辑节奏
说明 BGM 情绪、音效点、转场节奏、高潮点。

## AI 视频提示词
写成可复制的提示词，必须包含：
游戏类型、剧情冲突、关键动作、核心画面、镜头运动、光影质感、视频比例 ${data.ratio}、时长 ${data.duration}。

## 配音提示词
说明声音性别倾向、语速、情绪、停顿和重音。

## 结尾 CTA
给出 2-3 条不同强度的结尾召唤。`;
}

function buildMessages(data) {
  return [
    {
      role: "system",
      content: "你是游戏宣发短视频脚本专家。你必须用微剧情结构写脚本，只输出可执行、可拍、可剪的中文方案。"
    },
    {
      role: "user",
      content: buildScriptPrompt(data)
    }
  ];
}

module.exports = {
  buildMessages,
  buildScriptPrompt
};
